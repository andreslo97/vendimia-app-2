alter table public.profiles
add column if not exists phone_country_code text;

alter table public.profiles
add column if not exists phone_number text;

alter table public.profiles
add column if not exists can_manage_appointments boolean not null default false;

create unique index if not exists profiles_phone_unique
on public.profiles (phone_country_code, phone_number)
where phone_country_code is not null and phone_number is not null;

grant update (full_name, email, avatar_url, phone_country_code, phone_number) on public.profiles to authenticated;

create table if not exists public.phone_country_codes (
  id bigint generated by default as identity primary key,
  country_name text not null,
  country_code text not null,
  sort_order int default 0,
  is_active boolean default true
);

