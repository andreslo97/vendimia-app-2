alter table public.home_banners
add column if not exists badge_text text,
add column if not exists image_url text;

alter table public.events
add column if not exists background_color text,
add column if not exists accent_color text,
add column if not exists image_url text,
add column if not exists show_on_home boolean default false,
add column if not exists home_sort_order int default 0;

create table if not exists public.home_quick_links (
  id bigint generated by default as identity primary key,
  title text,
  route text,
  icon_name text,
  background_color text,
  accent_color text,
  sort_order int default 0,
  is_active boolean default true,
  created_at timestamptz default now()
);

alter table public.home_quick_links enable row level security;

drop policy if exists "home_quick_links_read_active" on public.home_quick_links;
create policy "home_quick_links_read_active"
on public.home_quick_links for select
to authenticated
using (is_active = true);

