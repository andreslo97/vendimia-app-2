update public.events
set
  background_color = coalesce(background_color, '#3A1F2A'),
  accent_color = coalesce(accent_color, '#F9820B'),
  image_url = coalesce(image_url, 'https://images.unsplash.com/photo-1517457373958-b7bdd4587205?auto=format&fit=crop&w=900&q=80')
where is_active = true;

with ranked_events as (
  select id, row_number() over (order by event_date asc, id asc) as rn
  from public.events
  where is_active = true
  and event_date >= (now() at time zone 'America/Bogota')::date
)
update public.events
set
  show_on_home = ranked_events.rn <= 2,
  home_sort_order = ranked_events.rn
from ranked_events
where events.id = ranked_events.id;
